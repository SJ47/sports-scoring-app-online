class Fixture:
    def __init__(self, home_team_id, away_team_id, fixture_date, fixture_result, league_id, id = None):
        self.home_team_id = home_team_id
        self.away_team_id = away_team_id
        self.fixture_date = fixture_date
        self.fixture_result = fixture_result
        self.league_id = league_id
        self.id = id

 